<h1 class="monospace" >Techniques </h1>
<hr>
<br>
<p>
  Le Vovinam Viet Vo Dao est un art martial très complet qui englobe une grande variété de techniques d’attaque, de self défense et de contre-attaque dont l’apprentissage se fait avec un partenaire ; dans la liste suivante on cite quelques techniques :
 </p>


<!-- position-->

<div id="position">
<h2 class="souligner">Position</h2>
<p>
Les positions sont très importantes au vovinam, les techniques reposant toujours sur un bon placement et un bon déplacement du corps.On retrouve des positions communes à la plupart des arts martiaux .
</p>
<img  class="displayedd" src="images/6.jpg" >
</div>

<!-- technique d'attaques -->

<div id="Technique d’attaques">
<h2 class="souligner">Technique d’attaques</h2>
  <p>
  Les techniques d’attaques sont des enchainements codifiés qu’on travaille à 2, mêlant des techniques de frappes, des blocages et des balayages. Il existe 30 techniques d’attaques au total </p>
  <img class="displayedd" src="images/9.jpg" >
</div>

<!-- contre attaques-->
<div id="Contre-attaque">
<h2 class="souligner">Contre-attaque </h2>
  <p>
  Les contres attaques se travaillent également à 2 et consistent à bloquer différents types d’attaques (coups de poings, coups de pieds) et attaquer en retour immédiatement
  </p>
  <img class="displayedd" src="images/10.jpg" >
</div>

<!-- projection -->
<div id="projection">
<h2 class="souligner"> Projection   </h2>

  <p>
  Les techniques de projections consistent à déstabiliser l’adversaire pour le faire basculer au-dessus des hanches ou du dos par exemple, comme certainestechniques qu’on retrouve au Judo. Les projections sont parfois des finalitéssur des techniques d’attaques ou des contre-attaques. Certaines projectionssont autorisées en compétitions combat .</p>
<img class="displayedd" src="images/11.jpg">
</div>

<!-- Armes -->

<div id="Armes">
<h2 class="souligner"> Armes    </h2>

<p>L’apprentissage de nombreuses armes fait partie du programme officiel de Vovinam. Les plus connues sont le couteau, le sabre long et le bâton. Mais on compte aussi des armes plus traditionnelles comme le sabre large, la hallebarde, l’éventail ou encore la règle </p>
<img class="displayedd" src="images/12.jpg" >
</div>

<!--siceaux-->
<div id="Siceaux">
<h2 class="souligner"> Siceaux    </h2>

<p>
Les ciseaux sont la spécificité du Vovinam Viet Vo Dao. Les ciseaux se travaillent de 2 façons : soit sous la forme de coups de pied sautés, soit en crochetant les jambes, le corps ou la tête de l’adversaire  </p>
<img class="displayedd" src="images/13.jpg" >
</div>
