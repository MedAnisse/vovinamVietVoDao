        <div id="Fondation">
            <h1 class="monospace" >Fondation </h1>
            <hr>
            <br>

            <p>
                Le Viet Vo Dao fut fondé il y a plusieurs millénaires par l'Empereur du Vietnam Hung Vuong 1er (vers 2879 avant JC). Il disparu durant la dynastie des Nguyen (1802-1945), lors de la colonisation française.
            </p>
            <a target="_blank" href="images/nguyen.png">
                <img class="image1right"src="images/nguyen.png"></a>
            <p>
                Dans les années 1930, Maître Nguyen Loc (1912-1960) chercha à donner un nouvel essor au Vovinam Viet Vo Dao. Après bien des changements et des codifications, le Vovinam Viet Vo Dao réapparait à partir de 1938 et est officialisé 7 ans plus tard en 1945 à Hanoï. Il devient alors un grand mouvement de jeunesse et non plus seulement un art martial. Il est un mouvement international d'éducation; "Un Art de Vivre".
            </p>

            <p>
                A la mort de Maître Nguyen Loc en 1960, le Vovinam Viet Vo Dao connut à nouveau une période d'accalmie. Il fallut attendre 1964, une nouvelle structure du Viet Vo Dao mise en place par Maître Phan Hoang, disciple de Maître Nguyen Loc. On assiste dès lors à la mondialisation de cet art martial.
                En France, le Vovinam Viet Vo Dao connait un grand développement national depuis 1975, sous l'impulsion de Maître Tran Nguyen Dao.
            </p>
            <h1 class="monospace" >Philosophie du  vovinam Viet Vo Dao </h1>
            <hr>
            <br>
            <p>
                Le Viet Vo Dao possède tout un systeme philosophique riche et structuré. Sur le chemin de cette voie, chaque pratiquant découvrira parallèlement à son évolution technique , la profondeur de la philosophie du <strong>Viet Vo Dao</strong>, enrichie et approfondie par les maîtres qui ont vécu ces expériences mentales. </p>
            <p>Actuellement, face aux armes blanches, la lutte avec les mains, les pieds, le corps ou les armes blanches est dérisoire. Il s'agit bien plutot de développer , de cultiver des  qualités telles que  la souplesse , la détermination, le réflexe.</p>
            <p> La pratique de l’art martial <strong> Viet Vo Dao </strong>a pour but la formation de l’homme tant sur le plan physique que mental. Il s’agit donc de cultiver la résistance , la santé pour être capable de riposter, de subir et de dépasser toutes les agressions extérieures.
            <ul>
                <li>Son but : La formation de l'homme vrai.</li>
                <li> Son principe : Harmonie en toutes choses.</li>
                <li> Sa devise : Être fort pour être utile.</li>
                <li>Son salut : La main d'acier sur le coeur de bonté.</li>
                <li>Son symbole : Le bambou, (souplesse et de droiture).</li>
            </ul>

            </p>

            <h1 class="monospace" >Les principes du vovinam  </h1>
            <hr>
            <br>

            <ol>
                <li>Atteindre le plus haut niveau de l'art pour servir l'Humanité. </li>
                <li>Être fidèle à l'idéal du Viet Vo Dao et être dévoué à sa cause. </li>
                <li>Être toujours unis, respecter les Maîtres et les Aînés, aimer les condisciples. </li>
                <li>Respecter rigoureusement la discipline, placer l'honneur au-dessus de tout. </li>
                <li>Respecter les autres arts et n'utiliser le Viet Vo Dao que pour la légitime défense. </li>
                <li>Cultiver la connaissance, forger l'esprit, progresser dans la voie. </li>
                <li>Vivre avec probité, simplicité, fidélité et noblesse d'esprit. </li>
                <li>Développer une volonté d'acier, vaincre les difficultés.</li>
                <li>Être lucide, persévérant et actif.</li>
                <li>Être maitre de soi-même, modeste, respectueux, tolérant et progresser en se jugeant soi-même.</li>

            </ol>


