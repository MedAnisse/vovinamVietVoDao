<h1 class="monospace">Arbitre </h1>
<hr>
<br>
<P>
L’arbitre est responsable, sur le tapis, du déroulement régulier du combat qu’il doit diriger selon les règles. Il doit se faire respecter des concurrents et exercer sur eux une pleine autorité, afin qu’ils obéissent immédiatement à ses ordres et instructions, comme il doit conduire le combat sans tolérer aucune intervention extérieure irrégulière et inopportune. Il doit exprimer ses directives en utilisant le vocabulaire international et travailler en collaboration étroite avec le juge. Il doit mener son action dans la direction du combat en se gardant d’intervenir de façon irréfléchie ou intempestive. Son coup de sifflet commence, interrompt et termine le match
</P>
<img class="displayedd" src="images/note.png">
<h1 class="monospace">Juge </h1>
<hr>
<br>
<P>
Le juge assure toutes les fonctions prévues par les règles de lutte en général. Il doit suivre très attentivement le déroulement du combat sans se laisser distraire d’aucune façon et attribuer les points de chaque action, qu’il consigne sur son bulletin de pointage, en accord avec l’arbitre ou le chef de tapis. Il doit donner son opinion dans toutes les situations.
Les juges annoncent leurs scores provisoires sur ordre de l’arbitre, Entre chaque reprise afin d’informer les compétiteurs et le public ; le vainqueur est celui qui a marqué le plus de points à l’issue de la ou des reprises.
</P>



