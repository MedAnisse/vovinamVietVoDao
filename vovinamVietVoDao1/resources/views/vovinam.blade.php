<h1 class="monospace" >Aire de compétition</h1>
<hr>
<br>
  <p>
    Une aire de compétition est un carré de 8 mètres de côté, soit une surface de 64 mètres carrés. Pour les combats, cette aire est entourée d’une aire de sécurité de 1 mètre. La surface totale d’une aire de compétition combat couverte de tapis est donc un carré de 10 mètres de côté soit une surface totale de 100 mètres carrés. La surface de combat et l’aire de sécurité doivent être de couleurs distinctes. L’aire de sécurité doit être constituée de tapis rouges ou oranges, il ne doit pas y avoir d’obstacles (panneaux publicitaires, de murs, de piliers, etc.), risquant de nuire à la sécurité des compétiteurs. La figure suivantes illustre une aire de compétition :
