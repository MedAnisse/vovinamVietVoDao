<h1 class="monospace">Définition</h1>
<hr>
<br>
<br>
  <p>
  Le Vovinam Viet Vo Dao, Art martial Traditionnel Vietnamien, est l’oeuvre de tout un peuple engagé, tout au long de son histoire dans une lutte pour la définition et la préservation de son identité. Le terme Viet Vo Dao est créé en 1964 par le 1er Conseil National des Maitres du Vietnam.
     Littéralement le Vovinam Viet Vo Dao signifie :
<ul>Vovinam : art martial Vietnamien ;</ul>
<ul>Viet : le nom du peuple vietnamien mais aussi transcendant, supérieur ;</ul>
<ul>Vo : l’art martial ;</ul>
<ul>Dao : la voie, l’ensemble des principes de la vie et de sagesse conduisant à un but.</ul>
</p>
